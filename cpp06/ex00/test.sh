#!/bin/bash


declare -a input=(
    "nan" "nanf" "nanff" "" " "
    "inf" "inff" "-inf" "-inff" "+inf" "+inff" "infff" 
    "a" "A" "z" "Z" "-" "*" "asd"
    "0" "-0" "+0" "1" "-1" "+1"
    "12" "42" "65" "97"
    "2147483647" "2147483648"
    "-2147483648" "-2147483649"
    "60000000000000000000000000000000000000.0f"
    "600000000000000000000000000000000000000.0f"
    "600000000000000000000000000000000000000.0"
    "600000000000000000000000000000000000000"
    "600000000000000000000000000000000000001"
    "42.2"
    "42"
    "42.2f"
    "340282346638528859811704183484516925444.1"
    )

for i in "${input[@]}"
do
    printf "input: "
    printf "%s\n" "$i"
    ./convert "$i"
    printf "\n"
done
